public class Date2 {
    private int _day;
    private int _month;
    private int _year;

    public Date2(int day, int month, int year) {
        _day = day;
        _month = month;
        _year = year;
    }

    public int getDayInt() {
        return _day;
    }

    public int getMonthInt() {
        return _month;
    }

    public int getYearInt() {
        return _year;
    }
}
